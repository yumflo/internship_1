    <h2>※尋找專心的音符　勤益科大同步腦波實驗室有答案  </h2>
    <p><font size="3">
	<a href="http://w3.iiiedu.org.tw/ilc/rainbowfun/science.php#01"><font color="red">活動網址http://w3.iiiedu.org.tw/ilc/rainbowfun/science.php#01</font></a><br>
「學習要有動力才能成長，創意要靠實踐才有價值。」因著這兩句話，資策會的數位教育研究所和智高實業、大寶科技和兆豐文創合作，舉辦了一場為期五天，與科普同樂的科技盛會，讓孩子沉浸在求知的海洋中，尋找自己的一片廣闊天空。<br>
為期五天的課程裡，每個時刻都能讓孩子有滿滿的收穫。每天都有豐富的課程和專業的講師，讓孩子學會物理與化學的專業知識；透過分組行動與團隊合作，每個孩子都能學會和他人溝通與表達；藉由實際練習與操作，孩子不僅吸收了知識，更將知識留存在經驗中。<br>
認識了自己的大腦，學會控制自己的專注與放鬆力，孩子們藉由大寶科技所提供的腦波儀看見自己的狀態在螢幕上呈現出來，驚訝之餘更想學會如何操控。再看看腦波放鬆燈所變化的顏色，孩子慢慢地學會專注地學習和身心靈的放鬆，等到訓練一定程度，重頭戲就要來囉！<br>
為了讓團隊所訓練的車子能順利地快速前進，孩子們費盡心思去思考和討論，打造出最輕最帥最時髦的樂高車；再選出最能控制自己腦波專注力的人選，比賽就此開始！每個人的專注程度在眉頭上可知，有的人不到十秒便笑了出來，也有人可以讓帥氣的樂高車走完全程，專注的程度高人一等！
